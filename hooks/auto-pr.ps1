# auto-pr.ps1 — create GitHub PR after first push to boss/ branch
# Called by auto-push.ps1 or directly after a push
param(
    [string]$CwdOverride = ""
)

$ErrorActionPreference = "SilentlyContinue"

# BOSS_SKIP bypass
if ($env:BOSS_SKIP -eq "1") {
    [Console]::Error.WriteLine("[BOSS auto-pr] BOSS_SKIP=1 -- skipped")
    exit 0
}

# Check gh CLI available
$ghCmd = Get-Command gh -ErrorAction SilentlyContinue
if (-not $ghCmd) {
    [Console]::Error.WriteLine("[BOSS auto-pr] WARNING: gh CLI not installed -- skipping PR creation")
    exit 0
}

# Check gh authenticated
$null = gh auth status 2>&1
if ($LASTEXITCODE -ne 0) {
    [Console]::Error.WriteLine("[BOSS auto-pr] WARNING: gh CLI not authenticated -- skipping PR creation")
    exit 0
}

# CWD
$cwd = if ($CwdOverride) { $CwdOverride } else { (Get-Location).Path }
Push-Location $cwd

try {
    # Read requirement title from CLAUDE.md
    $title = "BOSS: automated requirement"
    $claudeMd = Join-Path $cwd "CLAUDE.md"
    if (Test-Path $claudeMd) {
        $lines = Get-Content $claudeMd -ErrorAction SilentlyContinue
        foreach ($line in $lines) {
            $trimmed = $line.Trim()
            if ($trimmed -match '^[Rr]equirement[:\s]+(.+)$') {
                $title = $Matches[1].Trim()
                break
            }
            if ($trimmed -and $trimmed -notmatch '^#' -and $trimmed.Length -gt 3) {
                $title = $trimmed
                break
            }
        }
    }

    # Idempotent: check if PR already exists for this branch
    $currentBranch = git branch --show-current 2>$null
    $existing = gh pr list --head $currentBranch --json number --jq '.[0].number' 2>$null
    if ($existing -and $existing.Trim() -ne "" -and $existing.Trim() -ne "null") {
        [Console]::Error.WriteLine("[BOSS auto-pr] PR already exists (#$($existing.Trim())) -- skipping")
        exit 0
    }

    # Build PR body with spec.md summary
    $specSummary = "(spec.md not yet generated)"
    $specPath = Join-Path $cwd ".boss\spec.md"
    if (Test-Path $specPath) {
        $specLines = Get-Content $specPath -First 15 -ErrorAction SilentlyContinue
        $specSummary = $specLines -join "`n"
    }

    $certPath = Join-Path $cwd ".boss\certification.json"
    $ciStatus = if (Test-Path $certPath) {
        $cert = Get-Content $certPath -Raw | ConvertFrom-Json
        if ($cert.certified) { "certified" } else { "not certified" }
    } else { "pending" }

    $body = "## Requirement`n$title`n`n## Spec Summary (.boss/spec.md)`n$specSummary`n`n## CI Status`nBadge: see GitHub Actions`nCertification: $ciStatus`n`n*Generated by BOSS auto-pr hook*"

    # Create PR
    $result = gh pr create --title $title --body $body 2>&1
    if ($LASTEXITCODE -eq 0) {
        [Console]::Error.WriteLine("[BOSS auto-pr] Created PR: $result")
    } else {
        [Console]::Error.WriteLine("[BOSS auto-pr] PR creation failed: $result")
    }
} finally {
    Pop-Location
}

exit 0
